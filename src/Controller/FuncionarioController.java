public class FuncionarioController {
    private String funcFullname, funcUsername, funcPassword;
    private int id;

    /**
     * @return the funcFullname
     */
    public String getFuncFullname() {
        return funcFullname;
    }

    /**
     * @param funcFullname the funcFullname to set
     */
    public void setFuncFullname(String funcFullname) {
        this.funcFullname = funcFullname;
    }

    /**
     * @return the funcUsername
     */
    public String getFuncUsername() {
        return funcUsername;
    }

    /**
     * @param funcUsername the funcUsername to set
     */
    public void setFuncUsername(String funcUsername) {
        this.funcUsername = funcUsername;
    }

    /**
     * @return the funcPassword
     */
    public String getFuncPassword() {
        return funcPassword;
    }

    /**
     * @param funcPassword the funcPassword to set
     */
    public void setFuncPassword(String funcPassword) {
        this.funcPassword = funcPassword;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
    
    
}
